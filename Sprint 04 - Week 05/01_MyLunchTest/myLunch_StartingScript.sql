-- CREATE THE L_EMPLOYEES TABLE
DROP TABLE IF EXISTS L_EMPLOYEES;
GO
CREATE TABLE L_EMPLOYEES
(EMPLOYEE_ID    INT NOT NULL,
FIRST_NAME      VARCHAR(10),
LAST_NAME       VARCHAR(20),
DEPT_CODE       VARCHAR(3),
HIRE_DATE       DATE,
CREDIT_LIMIT    decimal(4,2),
PHONE_NUMBER    VARCHAR(4),
MANAGER_ID      INT);

ALTER TABLE L_EMPLOYEES
ADD CONSTRAINT PK_L_EMPLOYEES
PRIMARY KEY (EMPLOYEE_ID);

-- CREATE THE L_FOODS TABLE
DROP TABLE IF EXISTS L_FOODS;
GO
CREATE TABLE L_FOODS
(SUPPLIER_ID    VARCHAR(3) NOT NULL,
PRODUCT_CODE    VARCHAR(2) NOT NULL,
MENU_ITEM       INT,
DESCRIPTION     VARCHAR(20),
PRICE           decimal(4,2),
PRICE_INCREASE  decimal(4,2)
);

ALTER TABLE L_FOODS
ADD CONSTRAINT PK_L_FOODS
PRIMARY KEY (SUPPLIER_ID, PRODUCT_CODE);

-- CREATE THE L_DEPARTMENTS TABLE
